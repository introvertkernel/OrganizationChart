<script>
$(document).ready(function(){  
  $('#report_manager').keyup(function(){  
        var query = $(this).val();  
        if(query != '')  
        {  
            $.ajax({
                  url:"find.php",  
                  method:"POST",  
                  data:{query:query},  
                  success:function(data)  
                  {  
                      $('#managerList').fadeIn();  
                      $('#managerList').html(data);  
                  }
            });  
        }
  });  
  $(document).on('click', 'li', function(){  
        $('#report_manager').val($(this).text());  
        $('#managerList').fadeOut();  
  });  
});
</script>